package Lec_1;

public class code2_Arithmetic_Operation {

	public static void main(String[] args) {
		
		System.out.println("========================================");
		int a = 7;
		System.out.println("Value a = 7");
		int b = 9;
		System.out.println("Value b = 9");
		int c = a + b;
		System.out.println("========================================");
		System.out.println("a + b is " + c);
		System.out.println("========================================");
		c = a - b;
		System.out.println("========================================");
		System.out.println("a - b is " + c);
		System.out.println("========================================");
		c = a * b;
		System.out.println("========================================");
		System.out.println("a * b is " + c);
		System.out.println("========================================");
		c =  a / b;
		System.out.println("========================================");
		System.out.println("a / b is " + c);
		System.out.println("========================================");
		c =  a % b;
		System.out.println("========================================");
		System.out.println("a % b is " + c);
		System.out.println("========================================");
	}

}
